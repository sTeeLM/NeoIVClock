static void sm_clock_update_oled(bool is_oled_a)
{
  char str_buf[7];
  float fbuf[SENSOR_DATA_BUFFER_SIZE];
  uint16_t ibuf[SENSOR_DATA_BUFFER_SIZE];

  if(is_oled_a) {
    // 温度、湿度、气压
    sensor_data_get_temp(fbuf);
    snprintf(str_buf, sizeof(str_buf), "%6.2f",  config_read_int("temp_unit") ? fbuf[0] : cext_celsius_to_fahrenheit(fbuf[0]));
    str_buf[6] = 0;
    oled_ext_draw_string(48, 4, str_buf, OLED_DRAW_OVERWRITE);
    NEO_LOGD(TAG, "temp: %s", str_buf);

    sensor_data_get_mol(fbuf);
    snprintf(str_buf, sizeof(str_buf), "%6.2f",  fbuf[0]); 
    str_buf[6] = 0;
    oled_ext_draw_string(48, 24, str_buf, OLED_DRAW_OVERWRITE);
    NEO_LOGD(TAG, "mol: %s", str_buf);

    sensor_data_get_press(fbuf);
    snprintf(str_buf, sizeof(str_buf), "%6.4f",  config_read_int("press_unit") ? cext_pa_to_mmhg(fbuf[0]) : fbuf[0] / 1000000.0f);
    str_buf[6] = 0;
    oled_ext_draw_string(48, 44, str_buf, OLED_DRAW_OVERWRITE);
    NEO_LOGD(TAG, "press: %s", str_buf);

  } else {
    // PM2.5、TVOC、甲醛
    sensor_data_get_pm25(ibuf);
    snprintf(str_buf, sizeof(str_buf), "%6d",  ibuf[0]);
    str_buf[6] = 0;
    oled_ext_draw_string(48, 4, str_buf, OLED_DRAW_OVERWRITE);
    NEO_LOGD(TAG, "pm2.5: %s", str_buf);

    sensor_data_get_tvoc(fbuf);
    snprintf(str_buf, sizeof(str_buf), "%6.4f",  fbuf[0]); 
    str_buf[6] = 0;
    oled_ext_draw_string(48, 24, str_buf, OLED_DRAW_OVERWRITE);
    NEO_LOGD(TAG, "tvoc: %s", str_buf);

    sensor_data_get_form(fbuf);
    snprintf(str_buf, sizeof(str_buf), "%6.4f",  fbuf[0]);
    str_buf[6] = 0;
    oled_ext_draw_string(48, 44, str_buf, OLED_DRAW_OVERWRITE);
    NEO_LOGD(TAG, "form: %s", str_buf);
  }
}

static void sm_clock_draw_oled(bool is_oled_a)
{
  oled_clear();
  if(is_oled_a) {
    // 温度、湿度、气压
    oled_draw_bitmap(0, 2, 48, 20, config_read_int("temp_unit") ? CHAR_WENDU_SHESHI : CHAR_WENDU_HUASHI, OLED_DRAW_OVERWRITE);
    oled_draw_bitmap(0, 22, 48, 20, CHAR_SHIDU, OLED_DRAW_OVERWRITE);
    oled_draw_bitmap(0, 42, 48, 20, config_read_int("press_unit") ? CHAR_PRESS_HG : CHAR_PRESS_MP, OLED_DRAW_OVERWRITE);
  } else {
    // PM2.5、TVOC、甲醛
    oled_draw_bitmap(0, 2, 48, 20, CHAR_PM25, OLED_DRAW_OVERWRITE);
    oled_draw_bitmap(0, 22, 48, 20, CHAR_TVOC, OLED_DRAW_OVERWRITE);
    oled_draw_bitmap(0, 42, 48, 20, CHAR_JIAQUAN, OLED_DRAW_OVERWRITE);
  }
 
  sm_clock_update_oled(is_oled_a);
}

static void sm_do_clock(bool is_time, bool is_oled_a, task_event_t ev)
{
  if(ev == EV_V1) {
    // update oled
    sm_clock_update_oled(is_oled_a);
  } else if (ev == EV_1S) {
    // test timeo
    iv18_test_ps_timeo();
  } else if (ev == EV_ACC) {
    iv18_reset_ps_timeo();
  } else {
    iv18_reset_ps_timeo();
    clock_set_display_mode(is_time ? CLOCK_DISPLAY_MODE_TIME : CLOCK_DISPLAY_MODE_DATE);
    sm_clock_draw_oled(is_oled_a);
  }
}

void do_clock_init(uint8_t from_func, uint8_t from_state, uint8_t to_func, uint8_t to_state, task_event_t ev)
{
  iv18_clr();
  iv18_reset_ps_timeo();
  clock_set_display_mode(CLOCK_DISPLAY_MODE_TIME);
  // draw oled
  sm_clock_draw_oled(true);
}

static void do_clock_time_a(uint8_t from_func, uint8_t from_state, uint8_t to_func, uint8_t to_state, task_event_t ev)
{
  sm_do_clock(true, true, ev);
}

